package engine

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestDiscoverComposeServices(t *testing.T) {
	tempDir := t.TempDir()

	yamlContent := `
services:
  web:
    image: nginx
  db:
    image: postgres
`
	err := os.WriteFile(filepath.Join(tempDir, "docker-compose.yml"), []byte(yamlContent), 0644)
	if err != nil {
		t.Fatalf("Failed to write compose file: %v", err)
	}

	services, foundPath, err := DiscoverComposeServices(tempDir)
	if err != nil {
		t.Fatalf("DiscoverComposeServices failed: %v", err)
	}

	if filepath.Base(foundPath) != "docker-compose.yml" {
		t.Errorf("Expected found file 'docker-compose.yml', got '%s'", filepath.Base(foundPath))
	}

	if len(services) != 2 {
		t.Errorf("Expected 2 services, got %d", len(services))
	}

	hasWeb := false
	hasDb := false
	for _, s := range services {
		if s == "web" {
			hasWeb = true
		}
		if s == "db" {
			hasDb = true
		}
	}
	if !hasWeb || !hasDb {
		t.Errorf("Expected 'web' and 'db' services, got %v", services)
	}
}

