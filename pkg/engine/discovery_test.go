func TestGenerateDefaultChaosConfig(t *testing.T) {
	tempDir := t.TempDir()
	outPath := filepath.Join(tempDir, "chaos.yaml")
	services := []string{"svc1", "svc2"}

	err := GenerateDefaultChaosConfig(services, outPath)
	if err != nil {
		t.Fatalf("GenerateDefaultChaosConfig failed: %v", err)
	}

	content, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("Failed to read generated config: %v", err)
	}
	contentStr := string(content)

	if !strings.Contains(contentStr, "# Auto-generated by Entropy (entropy init)") {
		t.Error("Expected auto-generated header comment")
	}
	if !strings.Contains(contentStr, "- svc1") || !strings.Contains(contentStr, "- svc2") {
		t.Error("Expected generated config to contain the provided targets")
	}
}
